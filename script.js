function displayMovies(data) {
    document.getElementById("movies").innerHTML = "";

    let htmlString = ``;

    for (let i = 0; i < data.length; i++) {
        let embedUrl = data[i].trailer ? data[i].trailer.replace("watch?v=", "embed/") : "";

        htmlString += `
        <div class="movie" 
            onmouseover="playVideo(${i}, '${embedUrl}')" 
            onmouseleave="pauseVideo(${i})">
            
            <div class="overlay">
                <div class="video">
                    ${data[i].trailer ? `<iframe id="trailer-${i}" 
                        src="" 
                        frameborder="0" 
                        allowfullscreen 
                        allow="autoplay; encrypted-media"
                        style="display: none; width: 100%; height: 100%;">
                    </iframe>` : ""}
                </div>
                <div class="details">
                    <h1>${data[i].name}</h1> 
                    <h2>${data[i].ratting}</h2> 
                    <p>${data[i].Genre}</p>
                    <p>${data[i].ReleaseDate}</p>
                </div>
            </div>
            <img id="poster-${i}" src="${data[i].poster}" alt="poster"> 
        </div>`;
    }

    document.getElementById("movies").innerHTML = htmlString;
}

function playVideo(index, url) {
    let iframe = document.getElementById(`trailer-${index}`);
    let poster = document.getElementById(`poster-${index}`);
    if (iframe) {
        iframe.src = url + "?autoplay=1&mute=1&playsinline=1"; // Auto-play muted video
        iframe.style.display = "block"; // Show iframe
    }
    if (poster) {
        poster.style.display = "none"; // Hide poster when trailer plays
    }
}

function pauseVideo(index) {
    let iframe = document.getElementById(`trailer-${index}`);
    let poster = document.getElementById(`poster-${index}`);
    if (iframe) {
        iframe.src = ""; // Stop video playback
        iframe.style.display = "none"; // Hide iframe
    }
    if (poster) {
        poster.style.display = "block"; // Show poster again
    }
}

